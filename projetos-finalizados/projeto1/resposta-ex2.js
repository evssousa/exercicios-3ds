let num1 = parseInt(prompt('Digite um número:'))
let num2 = parseInt(prompt('Digite outro número:'))

let soma = num1 + num2
let sub = num1 - num2
let multi = num1 * num2
let div = num1 / num2

alert(`
    Resultado:
    ${num1} + ${num2} = ${soma}
    ${num1} - ${num2} = ${sub}
    ${num1} * ${num2} = ${multi}
    ${num1} / ${num2} = ${div}
`)